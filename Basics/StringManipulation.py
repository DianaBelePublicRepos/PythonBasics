s1 = "There is no such word as schmeedledorp"
s2 = "   a b c    "
s3 = "ABC"

print(" -> Capitalize first letter, the rest lowercase")
print(s3.capitalize())
print(" -> Count the number of spaces in s1")
print(s1.count(" "))
print(" -> Find the dot in S3")
print(s3.find("."))
print(" -> Is s2 all lower case?")
print(s2.islower())
print(" -> Strinp leading character from s2")
print(s2.lstrip())
print(" -> Strinp leading and trailing character from s2")
print(s2.strip())
print(" -> Swap the casing of letters in s1")
print(s1.swapcase())
print(" -> Show s1 in title case (initial caps)")
print(s1.title())
print(" -> Show s1 uppercase")
print(s1.upper())
